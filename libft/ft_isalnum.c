#include "libft.h"

int	ft_isalnum(char c)
{
	if (c >= 48 && c <= 57)
	       return (8);
	else if ((c >= 97 && c <= 122)
		|| (c >= 65 && c <= 90))
		return (8);
	return (0);
}
